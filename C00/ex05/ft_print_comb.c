#include<unistd.h>

void ft_print_comb(void)
{
    for(char a='0';a<='9';a++)
    {
        for(char b=a+1;b<='9';b++)
        {
            for(char c=b+1;c<='9';c++)
            {
                write(1,&a,1);
                write(1,&b,1);
                write(1,&c,1);
                if(a != '7')write(1,", ",2);
            }
        }
    }
}